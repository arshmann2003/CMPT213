package model;

/**
 * Get and change positions of cheese in maze
 */

public class Cheese {
    private int x;
    private int y;

    public Cheese(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}

